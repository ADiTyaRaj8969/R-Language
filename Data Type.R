#R-Data Type

#Logical Datatype
v<- TRUE
print(class(v))
print(v)

#Numeric Data Type
v<-23.5
print(class(v))
print (v)

#Integer Data Type
v<-2
print(class(v))
print (v)

#Complex Data Type
v<-2 + 5i
print(class(v))
print(v)

#Character
v<-"TRUE"
print(class(v))
print(v)

#Raw
v<-charToRaw("Aditya")
print(class(v))
print(v)

